export async function getStaticPaths() {
  const paths = blogs.map((blog) => ({
    params: { slug: blog.slug },
  }));

  return {
    paths,
    fallback: false,
  };
}

export async function getStaticProps({ params }) {
  const blog = blogs.find((blog) => blog.slug === params.slug);

  if (!blog) {
    return {
      notFound: true,
    };
  }

  return {
    props: { blog },
  };
}

